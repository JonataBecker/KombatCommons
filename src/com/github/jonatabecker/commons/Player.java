package com.github.jonatabecker.commons;

/**
 *
 * @author JonataBecker
 */
public class Player {
    
    private int x;
    
    private int y;

    public Player() {
        this.y = 100;
    }

    
    
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
